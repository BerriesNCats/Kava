package Variable.BuiltInVariables;

public class GroupVariables {
  /** Addresses all groups in a disallow_group() and allow_group() function */
  public static final String ALL_GROUPS = "$ALL_GROUPS";
  /** The total amount of groups in an instrument */
  public static final String NUM_GROUPS = "$NUM_GROUPS";
  /** An array with the group indexes of the groups that are affected by the current note event */
  public static final String GROUPS_AFFECTED = "%GROUPS_AFFECTED";
}
