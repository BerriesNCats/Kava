package Variable.BuiltInVariables;

public class EventVariables {
  /** Unique ID number of the event which triggered the callback */
  public static final String EVENT_ID = "$EVENT_ID";
  /** The note number of the event which triggered the callback */
  public static final String EVENT_NOTE = "$EVENT_NOTE";
  /** The velocity of the note that triggered the callback */
  public static final String EVENT_VELOCITY = "$EVENT_VELOCITY";

  /** An event parameter set with the set_event_par */
  public static final String EVENT_PARAMATERS = "%EVENT_PAR[]";
}
